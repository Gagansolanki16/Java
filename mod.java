class mod {
    public static void main(String[] args) {
        int a, b;
        a = 10;
        b = 8;
        System.out.println("Modulus of a and b: " + (a % b));

        double a1, b1;
        a1 = 198.21;
        b1 = 23.44;
        System.out.println("Modulus of a and b: " + (a1 % b1));
}
}
